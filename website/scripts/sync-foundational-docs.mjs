const docMap = new Map(publishedDocs.map((doc) => [doc.source, doc.output]));

function toPosixPath(value) {
  return value.replaceAll(path.sep, path.posix.sep);
}

function toRepoUrl(targetPath, fragment) {
  const normalized = toPosixPath(targetPath).replace(/^\.\//, '');
  const absoluteTarget = path.resolve(repoRoot, normalized);
  let routeKind = 'blob';

  if (normalized.endsWith('/')) {
    routeKind = 'tree';
  } else if (fs.existsSync(absoluteTarget)) {
    routeKind = fs.statSync(absoluteTarget).isDirectory() ? 'tree' : 'blob';
  }

  const encodedPath = normalized
    .split('/')
    .filter(Boolean)
    .map((segment) => encodeURIComponent(segment))
    .join('/');
  const suffix = fragment ? `#${fragment}` : '';
  return `${repoUrl}/${routeKind}/main/${encodedPath}${suffix}`;
}

function rewriteLinks(markdown, sourceFile) {
  return markdown.replace(/\[([^\]]+)\]\(([^)]+)\)/g, (match, label, rawTarget) => {
    if (!rawTarget || /^(?:[a-z]+:|#|\/)/i.test(rawTarget)) {
      return match;
    }

    const [rawPath, fragment = ''] = rawTarget.split('#');
    const normalizedTarget = toPosixPath(rawPath);
    const resolvedFromSource = path.posix.normalize(
      path.posix.join(path.posix.dirname(sourceFile), normalizedTarget),
    );

    if (docMap.has(resolvedFromSource)) {
      const destination = docMap.get(resolvedFromSource);
      return `[${label}](./${destination}${fragment ? `#${fragment}` : ''})`;
    }

    return `[${label}](${toRepoUrl(resolvedFromSource, fragment)})`;
  });
}

function buildFrontmatter(doc) {
  const sourceUrl = `${repoUrl}/blob/main/${doc.source}`;
  return [
    '---',
    `title: ${JSON.stringify(doc.title)}`,
    `sidebar_label: ${JSON.stringify(doc.title)}`,
    `description: ${JSON.stringify(doc.description)}`,
    `custom_edit_url: ${JSON.stringify(sourceUrl)}`,
    '---',
    '',
    '<!-- Generated by website/scripts/sync-foundational-docs.mjs -->',
    '',
    ':::note Canonical Root Document',
    `This page is generated from \`${doc.source}\` in the repository root. Edit the root file, then rerun \`npm --prefix website run sync:foundations\`.`,
    ':::',
    '',
  ].join('\n');
}

async function writeIfChanged(filePath, content) {
  const existing = await fsp.readFile(filePath, 'utf8').catch(() => null);
  if (existing === content) {
    return;
  }

  await fsp.writeFile(filePath, content, 'utf8');
}

async function syncDoc(doc) {
  const sourcePath = path.join(repoRoot, doc.source);
  const outputPath = path.join(outputDir, doc.output);
  const source = await fsp.readFile(sourcePath, 'utf8');
  const rewritten = rewriteLinks(source, doc.source);
  const content = `${buildFrontmatter(doc)}${rewritten.trimStart()}\n`;
  await writeIfChanged(outputPath, content);
}

async function main() {
  await fsp.mkdir(outputDir, {recursive: true});

  const existingEntries = await fsp.readdir(outputDir, {withFileTypes: true});
  await Promise.all(
    existingEntries
      .filter((entry) => entry.isFile())
      .map((entry) => fsp.unlink(path.join(outputDir, entry.name))),
  );

  for (const doc of publishedDocs) {
    await syncDoc(doc);
  }
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
